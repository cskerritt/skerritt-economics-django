# Helper functions
def get_state_by_slug(state_slug):
    """Get state data by slug"""
    for abbr, state_data in US_STATES_COMPLETE.items():
        if state_data['slug'] == state_slug:
            return abbr, state_data
    return None, None

def get_city_by_slug(state_abbr, city_slug):
    """Get city data by state and city slug"""
    if state_abbr not in US_STATES_COMPLETE:
        return None
    
    state_data = US_STATES_COMPLETE[state_abbr]
    for city in state_data['cities']:
        if city['slug'] == city_slug:
            return city
    return None

